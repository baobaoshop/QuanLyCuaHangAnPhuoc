    private void btnChonHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonHinhActionPerformed
        try {
            JFileChooser fileChooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Images(*.png;*.jpg;*.jpeg;*.gif;*.webp)", "png", "jpg", "jpeg", "gif", "webp");
            fileChooser.addChoosableFileFilter(filter);
            fileChooser.setAcceptAllFileFilterUsed(false);
            int kt = fileChooser.showOpenDialog(null);
            if (kt == JFileChooser.APPROVE_OPTION) {
                File f = fileChooser.getSelectedFile();
                tenHinh = f.getName();
                String location = "src/cuahanganphuoc/icon/" + tenHinh;
                File destinationF = new File(location);

                File desF = new File(location);
                if (!desF.exists()) {
                    Files.copy(f.toPath(), destinationF.toPath());
                }

                ImageIcon hinh = new ImageIcon(new ImageIcon(location).getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_DEFAULT));
                lblHinh.setIcon(hinh);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnChonHinhActionPerformed

    private void tbSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSPMouseClicked
        int row = tbSP.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tbSP.getModel();
        txtMa.setText((String) model.getValueAt(row, 0));
        txtTen.setText((String) model.getValueAt(row, 1));
        txtGia.setText(model.getValueAt(row, 2) + "");
        tenHinh = (String) model.getValueAt(row, 9);
        txtSoLuong.setText(model.getValueAt(row, 3).toString());
        ImageIcon hinh = new ImageIcon(new ImageIcon("src/cuahanganphuoc/icon/" + tenHinh).getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_DEFAULT));
        lblHinh.setIcon(hinh);
        txtTrangThai.setText(model.getValueAt(row, 4).toString());
        
        String size = model.getValueAt(row, 5).toString();
        String chatlieu = model.getValueAt(row, 7).toString();
        String kieudang = model.getValueAt(row, 6).toString();
        String mausac = model.getValueAt(row, 8).toString();
        String loai = model.getValueAt(row, 10).toString();
        SIZE s = ThamChieuDAO.getSIZEbyTENSIZE(size);
        CHATLIEU cl = ThamChieuDAO.getCHATLIEUbyTENCHATLIEU(chatlieu);
        KIEUDANG kd = ThamChieuDAO.getKIEUDANGbyTENKIEUDANG(kieudang);
        MAUSAC ms = ThamChieuDAO.getMAUSACbyTENMAUSAC(mausac);
        LOAISP lsp = ThamChieuDAO.getLOAISPCbyTENLOAISP(loai);
        cboSize.setSelectedItem(s);
        cboChatLieu.setSelectedItem(cl);
        cboMauSac.setSelectedItem(ms);
        cboKieuDang.setSelectedItem(kd);
        cboLoaiSanPham.setSelectedItem(lsp);
        TrangThai=2;
    }//GEN-LAST:event_tbSPMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(TrangThai!=1){
            TrangThai=1;
            txtMa.setText("");
            txtTen.setText("");
            txtGia.setText("");
            txtTrangThai.setText("");
            txtSoLuong.setText("");
            SanPhamDAO.loadAll();
            txtMa.setText(SanPhamDAO.taoMaKHMoi());
            return;
        }
        try{
            validateInputs();
        } catch(Exception e ){
            return;
        }
        String ten = txtTen.getText();
        String ma = txtMa.getText();
        int gia = Integer.parseInt(txtGia.getText());
        int sl = Integer.parseInt(txtSoLuong.getText());
        int tt = Integer.parseInt(txtTrangThai.getText());

        
        String size = ((SIZE)cboSize.getSelectedItem()).getMa();
        String kd = ((KIEUDANG)cboKieuDang.getSelectedItem()).getMaKD();
        String cl = ((CHATLIEU)cboChatLieu.getSelectedItem()).getMaCL();
        String ms = ((MAUSAC)cboMauSac.getSelectedItem()).getMaMS();
        String lsp = ((LOAISP)cboLoaiSanPham.getSelectedItem()).getMaLoai();

        SANPHAM u = new SANPHAM(ma, ten, lsp, ms, cl, kd, tenHinh, size, gia, tt, sl);

        if(!SanPhamDAO.themCSDL(u))
        {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
            return;
        }
        SanPhamDAO.loadAll();
        clear();
        loadSP();
        TrangThai=0;
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if(TrangThai!=2){
            JOptionPane.showMessageDialog(this, "Chưa sẵn sàng DELETE");
            return;
        }
        String ma = txtMa.getText();
        if(SanPhamDAO.xoaCSDL(ma)){
            JOptionPane.showMessageDialog(this, "THANH CONG");
        }else JOptionPane.showMessageDialog(this, "THAT BAI");
        SanPhamDAO.loadAll();
        loadSP();
        TrangThai=0;
    }//GEN-LAST:event_btnXoaActionPerformed

