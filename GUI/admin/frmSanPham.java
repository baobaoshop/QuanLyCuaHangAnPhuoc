    void initCbo(){
        for(SIZE s : LUUTRU.listSize){
            cboSize.addItem(s);
        }
        for(KIEUDANG s : LUUTRU.listKieDang){
            cboKieuDang.addItem(s);
        }
        for(CHATLIEU s : LUUTRU.listChatLieu){
            cboChatLieu.addItem(s);
        }
        for(MAUSAC s : LUUTRU.listMauSac){
            cboMauSac.addItem(s);
        }
        for(LOAISP s : LUUTRU.listLoaiSp){
            cboLoaiSanPham.addItem(s);
        }
    }
    void validateInputs() {
        String ma = txtMa.getText().trim();
         String ten = txtTen.getText().trim();
        String email = txtGia.getText().trim();
        String sdt = txtTrangThai.getText().trim();
        String sl = txtSoLuong.getText().trim();
    
         if (ma.isEmpty() || ten.isEmpty() || email.isEmpty() || sdt.isEmpty() || sl.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        throw new IllegalArgumentException("Vui lòng nhập đủ thông tin.");
         }
         try{
             int gia = Integer.parseInt(email);
             int tt = Integer.parseInt(sdt);
         }catch( Exception e){
             JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng các trường số.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        throw new IllegalArgumentException("Vui lòng nhập đúng định dạng các trường số.");
         }
    }
