    public void initTable() {
        LoaiSPDAO lspdao = new LoaiSPDAO();
        list = lspdao.layDanhSachLoaiSanPham();
        tblModel = (DefaultTableModel) tblLoaiSanPham.getModel();
        tblModel.setRowCount(0);  // Clear existing rows

        for (LOAISP loaisanPham : list) {
            Object[] row = new Object[]{
                loaisanPham.getMaLoai(), 
                loaisanPham.getTenLoai()
            };
            tblModel.addRow(row);
        }

        tblLoaiSanPham.setModel(tblModel);
    }
    public void Clear(){
        txtMaLoai.setText("");
        txtTenLoai.setText("");
        txtMaLoai.requestFocus();
    }
    void validateInputs() {
    String ma = txtMaLoai.getText().trim();
    String ten = txtTenLoai.getText().trim();
    
    if (ma.isEmpty() || ten.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        throw new IllegalArgumentException("Vui lòng nhập đủ thông tin.");
    }
    }
