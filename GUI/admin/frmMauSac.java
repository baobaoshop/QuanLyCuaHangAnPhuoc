    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(TrangThai!=1){
            TrangThai=1;
            txtMaMau.setText("");
            txtTenMau.setText("");
            MauSacDAO.loadAll();
            txtMaMau.setText(MauSacDAO.taoMaKHMoi());
            return;
        }
        try{
            validateInputs();
        } catch(Exception e ){
            return;
        }
        String ma = txtMaMau.getText().toString();
        String ten = txtTenMau.getText().toString();
        MauSacDAO  dao = new MauSacDAO();
        if(dao.themMauSac(ma,ten)){
            JOptionPane.showMessageDialog(this, "INSERT THANH CONG");
            Clear();
        }else {
            JOptionPane.showMessageDialog(this, "insert that bai");
        }
        initTable();
        TrangThai=0;
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if(TrangThai!=2){
            JOptionPane.showMessageDialog(this, "Chưa sẵn sàng UPDATE");
            return;
        }
        try{
            validateInputs();
        } catch(Exception e ){
            return;
        }
        String ma = txtMaMau.getText();
        String ten = txtTenMau.getText();
        MauSacDAO dao = new MauSacDAO();
        if (dao.suaMauSac(ten,ma)) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            Clear();
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
        initTable();
        TrangThai=0;
    }//GEN-LAST:event_btnSuaActionPerformed

