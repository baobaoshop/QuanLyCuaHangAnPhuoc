    public void initTable() {
        MauSacDAO msdao = new MauSacDAO();
        list = msdao.layDanhSachMAUSAC();
        tblModel = (DefaultTableModel) tblMauSac.getModel();
        tblModel.setRowCount(0);  // Clear existing rows

        for (MAUSAC mauSac : list) {
            Object[] row = new Object[]{
                mauSac.getMaMS(), 
                mauSac.getTenMS()
            };
            tblModel.addRow(row);
        }

        tblMauSac.setModel(tblModel);
    }
     public void Clear(){
        txtMaMau.setText("");
        txtTenMau.setText("");
        txtMaMau.requestFocus();
    }
     void validateInputs() {
        String ma = txtMaMau.getText().trim();
         String ten = txtTenMau.getText().trim();
    
         if (ma.isEmpty() || ten.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        throw new IllegalArgumentException("Vui lòng nhập đủ thông tin.");
         }
    }

