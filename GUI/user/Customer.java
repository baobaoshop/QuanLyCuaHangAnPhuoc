    void initBanDau(){
        btnXoa.setEnabled(false);
        txtMaKH.setEnabled(false);
        KhachHangDAO.loadAll();
        tblDanhSachKH.setDefaultEditor(Object.class, null);
        tblDanhSachKH.setSelectionMode(0);
        loadSP("");
    }
    private void loadSP(String key) {
        DefaultTableModel model = new DefaultTableModel();
        Vector data = new Vector();
        Vector<String> tieuDe = new Vector<String>();
        tieuDe.add("Mã khách hàng");
        tieuDe.add("Tên khách hàng");
        tieuDe.add("Email ");
        tieuDe.add("Số điẹn thoại ");

        List<KHACHHANG> dsMA = LUUTRU.listKhachHang;
        for (KHACHHANG ma : dsMA) {
            String maKH = ma.getMa();
            String tenKH = ma.getTen();
            String email = ma.getEmail();
            String sdt = ma.getSdt();
            if(maKH.contains(key) || tenKH.contains(key) || email.contains(key) || sdt.contains(key)){
                Vector<Object> v = new Vector<>();
                v.add(maKH);
                v.add(tenKH);
                v.add(email);
                v.add(sdt);
                data.add(v);
            }
        }

        if (data != null) {
            model.setDataVector(data, tieuDe);
        }
        tblDanhSachKH.setModel(model);
        clear();
        TrangThai=0;
        txtMaKH.setEnabled(false);
    }
    void clear(){
        txtMaKhachHang.setText("");
        txtMaKH.setText("");
        txtTenKH.setText("");
        txtEmailKH.setText("");
        txtSdtKH.setText("");
        txtMaKH.setEnabled(false);
    }
