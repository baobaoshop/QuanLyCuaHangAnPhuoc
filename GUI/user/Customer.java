    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(TrangThai!=1){
            TrangThai=1;
            clear();
            txtMaKH.setText(KhachHangDAO.taoMaKHMoi());
        } else {
            txtMaKH.setText(KhachHangDAO.taoMaKHMoi());
            String ma = txtMaKH.getText().toString();
            String ten = txtTenKH.getText().toString();
            String email = txtEmailKH.getText().toString();
            String sdt = txtSdtKH.getText().toString();
            if(ten.length()<=0 || email.length()<=0 || sdt.length()<=0){
                JOptionPane.showMessageDialog(this, "Dữ liệu không để trống");
                return;
            }
            if(!email.contains("@")){
                JOptionPane.showMessageDialog(this, "Email sai định dạng");
                return;
            }
            if(sdt.length()!=10){
                JOptionPane.showMessageDialog(this, "Số điện thoại 10 số");
                return;
            }
            try { 
                int sdtInt = Integer.parseInt(sdt);
            } catch(Exception e ){
                JOptionPane.showMessageDialog(this, "Số điện thoại không phải là số");
                return;
            }
            if(KhachHangDAO.ktSdtTrung(sdt)){
                JOptionPane.showMessageDialog(this, "Số điện thoại đã được sử dụng");
                return;
            }
            KHACHHANG kh = new KHACHHANG(ma, ten, email, sdt);
            if(!KhachHangDAO.themKH(kh)){
                JOptionPane.showMessageDialog(this, "Thất bại");
                return;
            }
            KhachHangDAO.loadAll();
            clear();
            loadSP("");
        }
    }//GEN-LAST:event_btnThemActionPerformed

