    private void btnChiTietActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChiTietActionPerformed
        String madh = txtMaDH.getText();
        DefaultTableModel model = new DefaultTableModel();
        
        Vector<String> tieuDe1 = new Vector<String>();
        tieuDe1.add("Mã đơn hàng");
        tieuDe1.add("Mã sản phẩm");
        tieuDe1.add("Tên sản phẩm");
        tieuDe1.add("Số lượng ");
        tieuDe1.add("Thành tiền");
        Vector data1 = new Vector();
        for(CT_DONHANG ct : LUUTRU.listCTDH){
            if(ct.getMaDH().equals(madh)){
                Vector<Object> v = new Vector<>();
                v.add(ct.getMaDH());
                v.add(ct.getMaSP());
                v.add(SanPhamDAO.getTenSanPhamByMa(ct.getMaSP().toString().trim()));
                v.add(ct.getSoLuong());
                v.add(ct.getThanhTien());
                data1.add(v);
            }
        }
        if (data1 != null) {
            model.setDataVector(data1, tieuDe1);
        }
        tblCTDH.setModel(model);
    }//GEN-LAST:event_btnChiTietActionPerformed

