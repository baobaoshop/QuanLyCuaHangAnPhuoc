    void initBanDau(){
        KhachHangDAO.loadAll();
        DonHangDAO.loadAll();
        CTDHDAO.loadAll();
        tblDH.setDefaultEditor(Object.class, null);
        tblDH.setSelectionMode(0);
        tblCTDH.setDefaultEditor(Object.class, null);
        tblCTDH.setSelectionMode(0);
        loadSP();
        txtMaDH.setEditable(false);
        txtMaKH.setEditable(false);
        txtNgayMua.setEditable(false);
        txtTongTien.setEditable(false);
        int tongthang =0;
        int tongngay=0;
        int tongall=0;
        Date now = new Date();
        for(DONHANG dh : LUUTRU.listDonHang){
            if(dh.getNgayMua().getDate()==now.getDate() && dh.getNgayMua().getMonth()==now.getMonth()&& dh.getNgayMua().getYear()==now.getYear()){
                tongngay+=dh.getTongTien();
            }
            if(dh.getNgayMua().getMonth()==now.getMonth()&& dh.getNgayMua().getYear()==now.getYear()){
                tongthang+=dh.getTongTien();
            }
            tongall+=dh.getTongTien();
        }
        txtTongNgay.setText("Tổng tiền theo ngày: "+tongngay);
        txtTongThang.setText(" - Tổng tiền theo tháng: "+tongthang);
        txtTongTienAll.setText(" - Tổng tiền tất cả: "+tongall);
    }
    private void loadSP() {
        DefaultTableModel model = new DefaultTableModel();
        Vector data = new Vector();
        Vector<String> tieuDe = new Vector<String>();
        tieuDe.add("Mã đơn hàng");
        tieuDe.add("Tên khách hàng");
        tieuDe.add("Ngày mua"); 
        tieuDe.add("Tổng tiền");


        List<DONHANG> dsMA = LUUTRU.listDonHang;
        for (DONHANG ma : dsMA) {
            String ten = ma.getMaDH();
            Date role = ma.getNgayMua();
            int tong = ma.getTongTien();
            
            String tenKH;
            if(ma.getMaKH().trim().length()<=0){
                tenKH = "Khách hàng vãng lãi";
            }
            else {
                tenKH = KhachHangDAO.getTENKHbyMAKH(ma.getMaKH());
            }

            Vector<Object> v = new Vector<>();
            v.add(ten);
            v.add(tenKH);
            v.add(role);
            v.add(tong);

            data.add(v);
        }

        if (data != null) {
            model.setDataVector(data, tieuDe);
        }
        tblDH.setModel(model);
        Clear();
    }
