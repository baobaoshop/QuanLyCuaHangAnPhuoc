    private void tblDanhSachDonHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhSachDonHangMouseClicked
        int row = tblDanhSachDonHang.getSelectedRow();
        txtMaKH.setText((String) dtm.getValueAt(row, 1));
        txtTenKH.setText(dtm.getValueAt(row, 2)+"");
        txtNgayLap.setText(dtm.getValueAt(row, 3) + "");
        txtTongTien.setText(dtm.getValueAt(row, 4)+"");
        
        String madh = dtm.getValueAt(row, 0).toString();
        DefaultTableModel model = new DefaultTableModel();
        
        Vector<String> tieuDe1 = new Vector<String>();
        tieuDe1.add("Mã đơn hàng");
        tieuDe1.add("Mã sản phẩm");
        tieuDe1.add("Tên sản phẩm");
        tieuDe1.add("Số lượng ");
        tieuDe1.add("Thành tiền");
        Vector data1 = new Vector();
        for(CT_DONHANG ct : LUUTRU.listCTDH){
            if(ct.getMaDH().equals(madh)){
                Vector<Object> v = new Vector<>();
                v.add(ct.getMaDH());
                v.add(ct.getMaSP());
                v.add(SanPhamDAO.getTenSanPhamByMa(ct.getMaSP().toString().trim()));
                v.add(ct.getSoLuong());
                v.add(ct.getThanhTien());
                data1.add(v);
            }
        }
        if (data1 != null) {
            model.setDataVector(data1, tieuDe1);
        }
        tblChiTiet.setModel(model);
    }//GEN-LAST:event_tblDanhSachDonHangMouseClicked

