    private void loadSP(String key) {
        dtm.setColumnCount(0);
        Vector data = new Vector();
        tieuDe.add("Mã đơn hàng");
        tieuDe.add("Mã khách hàng");
        tieuDe.add("Tên khách hàng");
        tieuDe.add("Ngày mua");
        tieuDe.add("Tổng tiền");

        List<DONHANG> dsMA = LUUTRU.listDonHang;
        for (DONHANG ma : dsMA) {
            String maDH = ma.getMaDH();
            String maKH = ma.getMaKH();
            String tenKH;
            if(ma.getMaKH().trim().length()<=0){
                tenKH = "Khách hàng vãng lãi";
            }
            else {
                tenKH = KhachHangDAO.getTENKHbyMAKH(ma.getMaKH());
            }
            String ngaymua = String.valueOf(ma.getNgayMua());
            String tong = ""+ma.getTongTien();
            if(maDH.contains(key) || maKH.contains(key) || tenKH.contains(key) || ngaymua.contains(key)){
                Vector<Object> v = new Vector<>();
                v.add(maDH);
                v.add(maKH);
                v.add(tenKH);
                v.add(ngaymua);
                v.add(tong);
                data.add(v);
            }
        }

        if (data != null) {
            dtm.setDataVector(data, tieuDe);
        }
        tblDanhSachDonHang.setModel(dtm);
        clear();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã đơn hàng");
        model.addColumn("Mã sản phẩm");
        model.addColumn("Tên sản phẩm");
        model.addColumn("Số lượng");
        model.addColumn("Thành tiền");
        tblChiTiet.setModel(model);
        
        
    }
    void initBanDau(){
        tblDanhSachDonHang.setDefaultEditor(Object.class, null);
        tblDanhSachDonHang.setSelectionMode(0);
        tblChiTiet.setDefaultEditor(Object.class, null);
        tblChiTiet.setSelectionMode(0);
        DonHangDAO.loadAll();
        CTDHDAO.loadAll();
        KhachHangDAO.loadAll();
        SanPhamDAO.loadAll();
        txtMaKH.setEditable(false);
        txtTenKH.setEditable(false);
        txtNgayLap.setEditable(false);
        txtTongTien.setEditable(false);
        clear();
    }
    void clear(){
        txtMaKH.setText("");
        txtTenKH.setText("");
        txtNgayLap.setText("");
        txtTongTien.setText("");
    }
