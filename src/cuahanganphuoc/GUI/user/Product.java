    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        DONHANG dh = new DONHANG();
        dh.setMaDH(DonHangDAO.taoMaDHMoi());
        if(isKH) {
            KHACHHANG kh = KhachHangDAO.getKhachHangBySDT(txtKhachHang.getText().toString());
            dh.setMaKH(kh.getMa());
        }else dh.setMaKH("");
        dh.setTongTien(Integer.parseInt(txtTongTien.getText().toString()));
        DonHangDAO.themHoaDon(dh);
        for(SANPHAMTAMTHOI sptt : listDonHang){
            CT_DONHANG ct = new CT_DONHANG(dh.getMaDH(), sptt.getMaString(), sptt.getSoLuong(), sptt.getThanhTien());
            CTDHDAO.themCTHoaDon(ct);
            SANPHAM sp = SanPhamDAO.getSanPhamByMa(ct.getMaSP());
            sp.truSoLuong(ct.getSoLuong());
            SanPhamDAO.capNhatCSDL(sp);
            
        }
        listDonHang.clear();
        initTableDonHang();
        JOptionPane.showMessageDialog(this, "OK");
        loadSP("", 0);
        txtMaSanPhamChinhSua.setText("");
        txtSoLuongChinhSua.setText("");
        initTableDonHang();
    }//GEN-LAST:event_btnThanhToanActionPerformed

