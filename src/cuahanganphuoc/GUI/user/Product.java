    private void btnCapNhatChinhSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatChinhSuaActionPerformed
        for(SANPHAMTAMTHOI sptt : listDonHang){
            if(sptt.getMaString().equals(txtMaSanPhamChinhSua.getText().toString())){
               int sl = Integer.parseInt(txtSoLuongChinhSua.getText().toString());
               SANPHAM sp = SanPhamDAO.getSanPhamByMa(txtMaSanPhamChinhSua.getText().toString());
               if(sl>sp.getSoluong()){
                   JOptionPane.showMessageDialog(this, "Số lượng sản phẩm trong kho không đáp ứng");
                   return;
               }
               if(sptt.getSoLuong()==sl){
                   JOptionPane.showMessageDialog(this, "Không thay đổi");
                   return;
               }
               if(sl==0){
                   listDonHang.remove(sptt);
                   initTableDonHang();
                   return;
               }
               listDonHang.add(new SANPHAMTAMTHOI(sp.getMa(), sp.getTen(), sl, sp.getGiaban()));
               listDonHang.remove(sptt);
               initTableDonHang();
               break;
            }
        }
    }//GEN-LAST:event_btnCapNhatChinhSuaActionPerformed

