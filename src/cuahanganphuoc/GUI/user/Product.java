    private void loadSP(String key, int sort) {
        dtm.setColumnCount(0);
        Vector data = new Vector();
        tieuDe.add("Mã sản phẩm");
        tieuDe.add("Tên sản phẩm");
        tieuDe.add("Giá bán");
        tieuDe.add("Số lượng");
        tieuDe.add("Size");
        tieuDe.add("Kiểu dáng");
        tieuDe.add("Chất liệu");
        tieuDe.add("Màu sắc");
        tieuDe.add("Hình");

        List<SANPHAM> dsMA = SanPhamDAO.showSANPHAM(key, sort);
        ThamChieuDAO references = new ThamChieuDAO();
        for (SANPHAM ma : dsMA) {
            Vector<Object> v = new Vector<>();
            v.add(ma.getMa());
            v.add(ma.getTen());
            v.add(String.valueOf(ma.getGiaban()));
            v.add(ma.getSoluong());
            v.add(ThamChieuDAO.getTENSIZEbyMASIZE(ma.getSize()));
            v.add(ThamChieuDAO.getTENKIEUDANGbyMAKIEUDANG(ma.getKieudang()));
            v.add(ThamChieuDAO.getTENCHATLIEUbyMACHATLIEU(ma.getChatlieu()));
            v.add(ThamChieuDAO.getTENMAUSACbyMAMAUSAC(ma.getMausac()));
            v.add(ma.getHinhanh());
            data.add(v);
        }

        if (data != null) {
            dtm.setDataVector(data, tieuDe);
        }
        
        if(tblDanhSachSanPham.getRowCount()>0){
            txtMaSanPham.setText((String) dtm.getValueAt(0, 0));
            txtTenSanPham.setText((String) dtm.getValueAt(0, 1));
            txtDonGia.setText(dtm.getValueAt(0, 2) + "");
            tenHinh = (String) dtm.getValueAt(0, 8);
            ImageIcon hinh = new ImageIcon(new ImageIcon("src/cuahanganphuoc/icon/" + tenHinh).getImage().getScaledInstance(txtHinhAnh.getWidth(), txtHinhAnh.getHeight(), Image.SCALE_DEFAULT));
            txtHinhAnh.setIcon(hinh);
            txtSoLuong.setText("1");
        }
    }
