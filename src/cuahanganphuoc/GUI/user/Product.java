    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        int soluong = Integer.parseInt(txtSoLuong.getText().toString());
        dtm = (DefaultTableModel) tblDanhSachSanPham.getModel();
        int slton = SanPhamDAO.getSoLuongTonTheoMa(txtMaSanPham.getText().toString());
        if(soluong>slton) {
            JOptionPane.showMessageDialog(this, "Sản phẩm tồn kho không đủ số lượng");
            return;
        }
        SANPHAM sp = SanPhamDAO.getSanPhamByMa(txtMaSanPham.getText().toString());
        
        for(SANPHAMTAMTHOI sptt : listDonHang){
            if(sptt.getMaString().equals(sp.getMa())){
                if(sptt.getSoLuong()+soluong>slton){
                    JOptionPane.showMessageDialog(this, "Sản phẩm tồn kho không đủ số lượng");
                    return;
                }
                listDonHang.add(new SANPHAMTAMTHOI(sp.getMa(), sp.getTen(), sptt.getSoLuong()+soluong, sp.getGiaban()));
                listDonHang.remove(sptt);
                initTableDonHang();
                return;
            }
        }
        
        SANPHAMTAMTHOI sp1;
        sp1 = new SANPHAMTAMTHOI(sp.getMa(), sp.getTen(), soluong, sp.getGiaban());
        listDonHang.add(sp1);
        initTableDonHang();
    }//GEN-LAST:event_btnThemActionPerformed

